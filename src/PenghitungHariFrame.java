    private void hitungHariDalamBulan() {
    try {
        int bulanIndex = comboBulan.getSelectedIndex() + 1; // Januari = 1
        int tahun = (Integer) spinnerTahun.getValue();

        YearMonth yearMonth = YearMonth.of(tahun, bulanIndex);
        int jumlahHari = yearMonth.lengthOfMonth();

        LocalDate hariPertama = yearMonth.atDay(1);
        LocalDate hariTerakhir = yearMonth.atEndOfMonth();

        // Format nama hari dalam Bahasa Indonesia
        Locale locale = new Locale("id", "ID");
        String namaHariPertama = hariPertama.getDayOfWeek().getDisplayName(TextStyle.FULL, locale);
        String namaHariTerakhir = hariTerakhir.getDayOfWeek().getDisplayName(TextStyle.FULL, locale);

        lblJumlahHari.setText("Jumlah Hari: " + jumlahHari);
        lblHariPertama.setText("Hari Pertama: " + namaHariPertama);
        lblHariTerkhir.setText("Hari Terakhir: " + namaHariTerakhir);

    } catch (Exception e) {
        lblJumlahHari.setText("Jumlah Hari: -");
        lblHariPertama.setText("Hari Pertama: -");
        lblHariTerkhir.setText("Hari Terakhir: -");
    }
}
