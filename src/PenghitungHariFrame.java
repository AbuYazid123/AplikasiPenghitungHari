    private void btlHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btlHitungActionPerformed
        Date mulai = dateMulai.getDate();
        Date akhir = dateAkhir.getDate();

        if (mulai != null && akhir != null) {
            long selisih = akhir.getTime() - mulai.getTime();
            long hari = selisih / (1000 * 60 * 60 * 24);
            txtSelisih.setText(hari + " hari");
        } else {
            JOptionPane.showMessageDialog(this, "Pilih kedua tanggal!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btlHitungActionPerformed

